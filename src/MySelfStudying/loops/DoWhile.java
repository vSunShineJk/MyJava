package MySelfStudying.loops;

public class DoWhile {
    public static void main(String[] args) {
        int count = 11;
        do {
            System.out.println("Hello world");
            count++;
        } while (count < 10);


        count = 11;
        while (count < 10) {
            System.out.println("Hello world");
            count++;
        }
    }

}
